# arithmetic
    # + => addition,
    # - => subtraction,
    # * => multiplication,
    # / => division,
    # % => modulus,
    # ** => exponential
    # // => floor division

n1=10
n2=20
print("sum=",n1+n2)
print("diff=",n1-n2)
print("prod=",n1*n2)
print("div=",n1/n2)
print("mod=",n1%n2) # 3/2 => return remainder =>1
print("exp=",n1**n2) # n1 to the power of n2 => n1 raise to n2
print("floor=",n1//n2) # 10//3 => (3) integer value

